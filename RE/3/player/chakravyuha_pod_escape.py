#!/usr/bin/env python3
import base64

BANNER = r'''
=== CHAKRAVYUHA: NETHER POD ESCAPE ===
You are trapped inside a pod under a ruined Minecraft fortress.
The pod accepts only one exact unlock key.
Find it, unlock the pod, and extract the hidden relay flag.
'''

# Fake bait for beginners using only plain text search.
DECOY_FLAG = "CH4KR4X2{d3c0y_d0nt_subm1t}"

# Tiny VM bytecode. Correct unlock key is reconstructed at runtime.
BYTECODE = [121, 96, 115, 103, 96, 125, 78, 127, 126, 99]

# XOR-encrypted + base64-encoded real flag.
FLAG_BLOB = "DQ1gAxdmB2I0FH4BYRczPwAifDIRdicrcSJsLQ=="


def pod_vm(bytecode):
    out = []
    for cell in bytecode:
        out.append(chr((cell ^ 0x2A) - 5))
    return "".join(out)


def decode_flag(blob, key):
    raw = base64.b64decode(blob.encode("ascii"))
    plain = []
    for i, b in enumerate(raw):
        plain.append(chr(b ^ ord(key[i % len(key)])))
    return "".join(plain)


def gate(candidate):
    return candidate.strip() == pod_vm(BYTECODE)


def main():
    print(BANNER)
    key = input("Enter pod unlock key: ")

    if gate(key):
        print("\\n[OK] Pod opened.")
        print("[OK] Relay output:", decode_flag(FLAG_BLOB, pod_vm(BYTECODE)))
    else:
        print("\\n[FAIL] Wrong key.")
        print("Hint 1: key length is 10")
        print("Hint 2: generated by a small transform")


if __name__ == "__main__":
    main()
